            console.log("点击了收藏按钮")
            console.log("当前页面音乐id为:",this.$store.state.music)
            collectapi.collect_music(this.$store.state.music).then(response => {
                console.log(response);
            }).catch(error => {
                console.error(error);
            });

        }
    },
    created() {
        this.audio = this.$store.state.audio
        this.getLyric()
        console.log("获取音乐信息：", this.$store.state.music)
        if (this.$store.state.music) {
            this.isDefaultView = true
        } else {
            console.log("未获取到音乐信息,显示默认视图")
        }
    },
    mounted() {

        // 监听音频播放状态变化
        this.audio.addEventListener('timeupdate', () => {
            this.currentTime = this.formatTime(this.audio.currentTime);
            // 保存到vux
            this.$store.commit("saveCurrentTime", this.$store.state.audio.currentTime)
            // console.log("当前音乐播放时间:", this.currentTime)
        });

        // 监听音频播放结束
        this.audio.addEventListener('ended', () => {
            console.log('音频播放结束');
            this.currentTime = 0;
        });
    },
}
